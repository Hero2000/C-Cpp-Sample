int add(int num1, int num2) {
    return num1 + num2;
}

double add(double num1, double num2) {
    return num1 + num2;
}

float add(float num1, float num2) {
    return num1 + num2;
}

//模版函数
template<typename T>
T add(T num1, T num2) {
    return num1 + num2;
}


